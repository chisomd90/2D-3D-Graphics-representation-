import turtle # import turtle module

# Set up the screen
screen = turtle.Screen()
screen.title("Original Triangle")

# Creating a turtle
tri = turtle.Turtle()

# This section will draw a triangle
tri.penup()
tri.goto(0, 0)
tri.pendown()
tri.goto(100, 0)
tri.goto(50, 87)
tri.goto(0, 0)


turtle.done()
